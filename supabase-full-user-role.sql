-- =====================================================
-- 5. Document Segments Table
-- RAG Vector Chunks
-- =====================================================
CREATE TABLE IF NOT EXISTS public.doc_segments (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

  document_id uuid REFERENCES public.documents(id) ON DELETE CASCADE,

  content text NOT NULL,
  embedding public.vector(768) NOT NULL,

  chunk_index integer,

  created_at timestamptz DEFAULT now() NOT NULL,

  CONSTRAINT doc_segments_content_len_check CHECK (
    char_length(content) <= 20000
  )
);

ALTER TABLE public.doc_segments ENABLE ROW LEVEL SECURITY;


DROP POLICY IF EXISTS "Admins have full access to doc segments" ON public.doc_segments;
DROP POLICY IF EXISTS "Users can view active document segments" ON public.doc_segments;


CREATE POLICY "Admins have full access to doc segments"
ON public.doc_segments
FOR ALL
USING (
  EXISTS (
    SELECT 1
    FROM public.profiles
    WHERE profiles.id = auth.uid()
    AND profiles.role = 'admin'
  )
)
WITH CHECK (
  EXISTS (
    SELECT 1
    FROM public.profiles
    WHERE profiles.id = auth.uid()
    AND profiles.role = 'admin'
