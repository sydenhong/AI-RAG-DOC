ON public.doc_segments
FOR SELECT
USING (
  EXISTS (
    SELECT 1
    FROM public.documents d
    WHERE d.id = doc_segments.document_id
    AND d.status = 'active'
  )
);


-- =====================================================
-- 6. Vector Search Function
-- No room_id
-- No source_message_id
-- Search only active documents
-- =====================================================
CREATE OR REPLACE FUNCTION public.match_doc_segments(
  query_embedding vector(768),
  match_threshold float,
  match_count int
)
RETURNS TABLE (
  id bigint,
  content text,
  document_id uuid,
  document_title text,
  category text,
  similarity float
)
LANGUAGE sql
STABLE
AS $$
  SELECT
    ds.id,
    ds.content,
    ds.document_id,
    d.title AS document_title,
    d.category,
    1 - (ds.embedding <=> query_embedding) AS similarity
  FROM public.doc_segments ds
  JOIN public.documents d ON ds.document_id = d.id
  WHERE d.status = 'active'
    AND 1 - (ds.embedding <=> query_embedding) > match_threshold
  ORDER BY ds.embedding <=> query_embedding
  LIMIT match_count;
$$;


